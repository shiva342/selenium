package selenium;

public class sample {

	public static void main(String[] args) {
	
			//create name 
			String name= "shiva";	
			System.out.println(name);
			
		//	using for loop for reverse order of string
		for (int i = name.length()-1;i>=0;i--) { //used length()-1 for finding last index number in string
			System.out.print(name.charAt(i));
			
			
			}}

	}

